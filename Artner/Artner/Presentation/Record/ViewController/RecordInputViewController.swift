    func imagePickerController(_ picker: UIImagePickerController, didFinishPickingMediaWithInfo info: [UIImagePickerController.InfoKey : Any]) {
        picker.dismiss(animated: true)
        
        guard let image = info[.editedImage] as? UIImage ?? info[.originalImage] as? UIImage else {
            print("üì∏ [RecordInputViewController] Ïù¥ÎØ∏ÏßÄ ÏÑ†ÌÉù Ïã§Ìå®")
            return
        }
        
        // ÏÑ†ÌÉùÎêú Ïù¥ÎØ∏ÏßÄÎ•º ViewModelÏóê Ï†ÄÏû•ÌïòÍ≥† ViewÏóê ÌëúÏãú
        viewModel.updateImage(image)
        recordInputView.showSelectedImage(image)
        
        print("üì∏ [RecordInputViewController] Ïù¥ÎØ∏ÏßÄ ÏÑ†ÌÉù ÏôÑÎ£å")
    }
    
    func imagePickerControllerDidCancel(_ picker: UIImagePickerController) {
        picker.dismiss(animated: true)
        print("üì∏ [RecordInputViewController] Ïù¥ÎØ∏ÏßÄ ÏÑ†ÌÉù Ï∑®ÏÜå")
    }
}
